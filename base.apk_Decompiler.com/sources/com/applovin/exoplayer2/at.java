package com.applovin.exoplayer2;

public final class at {
    public static final at hh = new at(false);
    public final boolean hi;

    public int hashCode() {
        return this.hi ^ true ? 1 : 0;
    }

    public at(boolean z) {
        this.hi = z;
    }

    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj != null && getClass() == obj.getClass() && this.hi == ((at) obj).hi) {
            return true;
        }
        return false;
    }
}
